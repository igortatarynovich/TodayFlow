    def interpret_house(self, house_number: int, sign: Optional[str] = None) -> Dict[str, str]:
        """Интерпретация дома."""
        house_info = self.HOUSE_MEANINGS.get(house_number, {
            "name": f"Дом {house_number}",
            "theme": "Неизвестная сфера",
            "description": ""
        })
        
        result = house_info.copy()
        if sign:
            result["sign_in_house"] = f"Знак {sign} в {house_number} доме добавляет свои качества к этой сфере"
        
        return result
    
    def _normalize_sign(self, sign: str) -> str:
        """Нормализует название знака (переводит с русского на английский если нужно)."""
        return self.SIGN_MAPPING.get(sign, sign)
    
    def interpret_planet_in_sign(self, planet: str, sign: str) -> Optional[str]:
        """Интерпретация планеты в знаке."""
        normalized_sign = self._normalize_sign(sign)
        planet_meanings = self.PLANET_IN_SIGN_MEANINGS.get(planet, {})
        return planet_meanings.get(normalized_sign)
    
    def interpret_planet_in_house(self, planet: str, house: int) -> str:
        """Интерпретация планеты в доме."""
        house_info = self.HOUSE_MEANINGS.get(house, {"theme": "неизвестная сфера"})
        template = self.PLANET_IN_HOUSE_TEMPLATES.get(planet, "{planet} в {house} доме: {house_meaning}")
        
        return template.format(
            planet=planet,
            house=house,
            house_meaning=house_info.get("theme", "")
        )
    
    def interpret_full_chart(self, chart: ChartResponse) -> Dict:
        """Полная интерпретация натальной карты."""
        interpretations = {
            "houses": {},
            "planets": [],
            "angles": {}
        }
        
        # Интерпретируем дома
        if chart.houses:
            for house_num in range(1, 13):
                house_data = (
                    chart.houses.get(f"house_{house_num}")
                    or chart.houses.get(str(house_num))
                    or chart.houses.get(house_num)
                )
                if house_data:
                    sign = house_data.get("sign") if isinstance(house_data, dict) else None
                    interpretations["houses"][house_num] = self.interpret_house(house_num, sign)
        
        # Интерпретируем планеты
        if chart.positions:
            for pos in chart.positions:
                if not isinstance(pos, dict):
                    continue
                
                body = pos.get("body") or pos.get("name")
                sign = pos.get("sign")
                house = pos.get("house")
                
                if not body:
                    continue
                
                planet_interpretation = {
                    "planet": body,
                    "sign": sign,
                    "house": house,
                    "degree": pos.get("degree"),
                    "in_sign": None,
                    "in_house": None
                }
                
                if sign:
                    planet_interpretation["in_sign"] = self.interpret_planet_in_sign(body, sign)
                
                if house:
                    planet_interpretation["in_house"] = self.interpret_planet_in_house(body, house)
                
                interpretations["planets"].append(planet_interpretation)
        
        # Интерпретируем углы (ASC, MC, IC, DSC)
