/**
 * Выбирает ритм наблюдения на основе времени и дня недели.
 * Используется как мягкий старт без персональных данных.
 */
export function selectColdState(): ColdState {
  const now = new Date();
  const hour = now.getHours();
  const dayOfWeek = now.getDay(); // 0 = воскресенье, 6 = суббота
  
  // Определяем часть суток
  let timeOfDay: "morning" | "day" | "evening" | "night";
  if (hour >= 6 && hour < 12) {
    timeOfDay = "morning";
  } else if (hour >= 12 && hour < 18) {
    timeOfDay = "day";
  } else if (hour >= 18 && hour < 22) {
    timeOfDay = "evening";
  } else {
    timeOfDay = "night";
  }
  
  // Глобальный паттерн дня (можно сделать более сложным)
  // Используем комбинацию дня недели и времени суток для детерминированного выбора
  const dayPattern = (dayOfWeek * 7 + hour) % COLD_STATES.length;
  
  return COLD_STATES[dayPattern];
}

/**
 * Получает состояние для конкретного дня (для зарегистрированных пользователей)
 * Используется для интеграции с 7-дневным сценарием
 */
export function getStateForDay(day: number): ColdState {
  // Для зарегистрированных пользователей можно использовать более сложную логику
  // Пока используем простой ротационный подход
  return COLD_STATES[day % COLD_STATES.length];
}

