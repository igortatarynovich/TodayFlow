export default function QuestionsPage() {
  const [hubContext, setHubContext] = useState<QuestionsHubContextResponse | null>(null);
  const preferredLane = hubContext?.preferred_lane || null;
  const hubCopy = preferredLane ? HUB_COPY[preferredLane] : null;

  useEffect(() => {
    const loadContext = async () => {
      try {
        const response = await getJson<QuestionsHubContextResponse>("/questions/context");
        setHubContext(response);
      } catch (error) {
        console.error("Failed to load questions hub context", error);
      }
    };
    void loadContext();
  }, []);

  return (
    <ProductPageScreen
      testId="questions-hub-page"
      title={hubCopy?.title || "Один вопрос — следующий шаг"}
      hideHeader
      contentClassName={`${pl.content} ${pl.legacyHost}`}
    >
      <QuestionEntryCard
        eyebrow={hubCopy?.eyebrow || "Questions"}
        title={hubCopy?.title || "Один вопрос — следующий шаг"}
        body={
          hubCopy?.body ||
          "Напиши, что сейчас важно. Получишь короткий разбор и понятное действие дальше."
        }
        placeholder={hubCopy?.placeholder || "Например: стоит ли мне сейчас писать этому человеку или лучше отступить?"}
        quickPrompts={hubCopy?.quickPrompts || QUICK_PROMPTS}
        primaryLabel={hubCopy?.primaryLabel || "Получить ответ"}
        secondaryHref="/today"
        secondaryLabel="Вернуться в Today"
        hubLaneHint={preferredLane || undefined}
        surfaceId="questions_hub"
      />
    </ProductPageScreen>
  );
}
